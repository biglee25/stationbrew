function openMenu() {
var slideMenu = document.getElementById("menu-wrapper");
if (slideMenu.style.display === "block") {
    slideMenu.style.display = "none";
} else {
    slideMenu.style.display = "block";
    }
}

function closeMenu() {
var slideMenu = document.getElementById("menu-wrapper");
if (slideMenu.style.display === "block") {
slideMenu.style.display = "none";
} else {
slideMenu.style.display = "block";

}
}


